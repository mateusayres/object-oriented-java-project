    private void botaoCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCalcularActionPerformed
        if (somatorioDeNotas == 0.00) {
            JOptionPane.showMessageDialog(null, "Insira os dados.");
        } else {
            try {
                AlunoController alunoController = new AlunoController();
                double resultado = alunoController.calcularMedia(somatorioDeNotas, contagemDaSoma);

                areaResultado.setText("-- RESULTADO DA MÃDIA --" + "\n\n"
                        + "SomatÃ³rio: " + somatorioDeNotas + "\n"
                        + "Quantidade de Alunos: " + contagemDaSoma + "\n"
                        + "Resultado: " + resultado);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Insira dados vÃ¡lidos.");
            }
        }

    }//GEN-LAST:event_botaoCalcularActionPerformed

