    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
        Aluno aluno = new Aluno();
        Date date = new Date();
        String dataAtual = Util.getDataPtBr(date);
        
        if (campoNome.getText().equals("") || campoCPF.getText().equals("") || campoMatricula.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha os campos primeiro.");
        } else {
            try {
                Long.parseLong(campoCPF.getText());
                Long.parseLong(campoMatricula.getText());
                
                aluno.setNome(campoNome.getText());
                aluno.setCpf(campoCPF.getText());
                aluno.setMatricula(campoMatricula.getText());
                
                areaResultado.setText("-- ALUNO REGISTRADO COM SUCESSO --" + "\n\n" +
                        "Nome: " + aluno.getNome() + "\n" +
                        "CPF: " + aluno.getCpf() + "\n" +
                        "Matr√≠cula: " + aluno.getMatricula() + "\n" +
                        "Registro: " + dataAtual);
                
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "CPF e Matr√≠cula devem conter apenas n√∫meros.");
            }
        }
    }//GEN-LAST:event_botaoCadastrarActionPerformed

